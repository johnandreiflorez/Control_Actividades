    private void btnProyectWithActivityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProyectWithActivityActionPerformed
        this.name = "Listado de Proyectos con sus actividades";
        tblMaster.setVisible(true);
        ListingsController listingsController = new ListingsController();
        String[][] matriz = listingsController.consultProyectWithActivity();
        
        lblTitle.setText("Listado de Proyectos con sus actividades");
        Object objMat[][] = new String[matriz.length][9];
        this.vecTit = new String[9];
        this.vecTit[0] = "Código Proyecto";
        this.vecTit[1] = "Título Proyecto";
        this.vecTit[2] = "Fecha Inicio Proyecto";
        this.vecTit[3] = "Fecha Fin Proyecto";
        this.vecTit[4] = "Costo Proyecto";
        this.vecTit[5] = "Código Actividad";
        this.vecTit[6] = "Descripción Actividad";
        this.vecTit[7] = "Fecha Inicio Actividad";
        this.vecTit[8] = "Fecha Fin Actividad";
        
        for(int i = 0; i < matriz.length; i++){
            objMat[i][0] = matriz[i][0];
            objMat[i][1] = matriz[i][1];
            objMat[i][2] = matriz[i][2];
            objMat[i][3] = matriz[i][3];
            objMat[i][4] = "$ " + matriz[i][4];
            objMat[i][5] = matriz[i][5];
            objMat[i][6] = matriz[i][6];
            objMat[i][7] = matriz[i][7];
            objMat[i][8] = matriz[i][8];
        }
        DefaultTableModel objModelo = new DefaultTableModel(objMat, this.vecTit);
        tblMaster.setModel(objModelo);
        btnExport.setEnabled(true);
    }//GEN-LAST:event_btnProyectWithActivityActionPerformed

