    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        String cadena = "";
        String data[] = null;
        data  = new String [tblMaster.getRowCount()];
        for(int i = 0; i < tblMaster.getRowCount(); i++){
            for(int j=0; j < tblMaster.getColumnCount(); j++){
                if(j == this.vecTit.length - 1) {
                    cadena += this.vecTit[j].toUpperCase() + ": " + tblMaster.getValueAt(i, j).toString();
                } else {
                    cadena += this.vecTit[j].toUpperCase() + ": " + tblMaster.getValueAt(i, j).toString() + ", ";
                }
            }
          data[i] = cadena;
          cadena = "";
        }
        
        Export objExport = new Export();
        objExport.exportToTxt(data, name);
    }//GEN-LAST:event_btnExportActionPerformed


